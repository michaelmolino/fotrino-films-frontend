import { execFile as execFileCallback } from 'node:child_process'
import { mkdir, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import { promisify } from 'node:util'

import { compile } from 'json-schema-to-typescript'

const execFile = promisify(execFileCallback)

const scriptDir = path.dirname(fileURLToPath(import.meta.url))
const frontendRoot = path.resolve(scriptDir, '..')
const backendRoot = path.resolve(frontendRoot, '../fotrino-films-backend')
const backendSrc = path.join(backendRoot, 'src')
const pythonExecutable = path.join(backendRoot, '.venv', 'bin', 'python')
const outputDir = path.join(frontendRoot, 'src', 'types')
const outputFile = path.join(outputDir, 'api.generated.d.ts')
const bannerComment = '/* This file is generated by yarn generate:api-contracts. Do not edit by hand. */'

function indentBlock(text, prefix = '  ') {
    return text
        .split('\n')
        .map(line => (line.length > 0 ? `${prefix}${line}` : line))
        .join('\n')
}

function wrapInternalDeclarations(compiled) {
    return `declare namespace ApiContractsInternal {\n${indentBlock(compiled.trim())}\n}`
}

function buildPublicAliases(schemaBundle) {
    const aliasLines = ['export type ApiContracts = ApiContractsInternal.ApiContracts']

    aliasLines.push(
        ...Object.keys(schemaBundle.properties || {})
            .map(name => `export type ${name} = ApiContracts['${name}']`)
    )

    return `\n/* Clean public aliases for root backend contracts. */\n${aliasLines.join('\n')}\n`
}

async function loadSchemaBundle() {
    const { stdout } = await execFile(
        pythonExecutable,
        ['-m', 'contracts.export_contract_schemas'],
        {
            cwd: backendSrc,
            maxBuffer: 10 * 1024 * 1024,
        }
    )
    return JSON.parse(stdout)
}

async function generateContracts() {
    const schemaBundle = await loadSchemaBundle()
    const compiled = await compile(schemaBundle, 'ApiContracts', {
        bannerComment: '',
        format: true,
        style: {
            singleQuote: true,
            semi: false,
            tabWidth: 2,
            printWidth: 100,
        },
        unreachableDefinitions: true,
    })
    const publicAliases = buildPublicAliases(schemaBundle)
    const wrappedDeclarations = wrapInternalDeclarations(compiled)

    await mkdir(outputDir, { recursive: true })
    await writeFile(
        outputFile,
        `${bannerComment}\n\n${wrappedDeclarations}${publicAliases}`.trimEnd() + '\n',
        'utf8'
    )
    console.log(`Generated ${path.relative(frontendRoot, outputFile)}`)
}

try {
    await generateContracts()
} catch (error) {
    console.error(error)
    process.exitCode = 1
}