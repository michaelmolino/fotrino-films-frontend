import { readFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const scriptDir = path.dirname(fileURLToPath(import.meta.url))
const frontendRoot = path.resolve(scriptDir, '..')
const contractsFile = path.join(frontendRoot, 'src', 'types', 'api.generated.d.ts')

const requiredContracts = [
    'AccountProfile',
    'AccountProvidersResponse',
    'ChannelSummary',
    'ChannelDetail',
    'ChannelMedia',
    'AdminUsersResponse',
    'UploadMediaRequest',
    'UploadValidationResponse',
    'MediaSession',
    'GlobalHttpErrorResponse'
]

function assertIncludes(text, value, message) {
    if (!text.includes(value)) {
        throw new Error(message)
    }
}

async function run() {
    const generated = await readFile(contractsFile, 'utf8')

    assertIncludes(
        generated,
        '/* This file is generated by yarn generate:api-contracts. Do not edit by hand. */',
        'Generated contracts banner is missing.'
    )
    assertIncludes(generated, 'export interface ApiContracts {', 'ApiContracts interface is missing.')

    for (const contractName of requiredContracts) {
        assertIncludes(
            generated,
            `  ${contractName}:`,
            `Missing required contract mapping in ApiContracts: ${contractName}`
        )
    }

    console.log(`Validated ${path.relative(frontendRoot, contractsFile)} successfully.`)
}

try {
    await run()
} catch (error) {
    console.error(error instanceof Error ? error.message : error)
    process.exitCode = 1
}
